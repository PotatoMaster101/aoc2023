namespace AoC.Common.ThreeDimensional;

/// <summary>
/// Represents a 3D direction.
/// </summary>
public enum Direction
{
    /// <summary>
    /// The direction pointing to negative X.
    /// </summary>
    NegativeX = 0,

    /// <summary>
    /// The direction pointing to negative Y.
    /// </summary>
    NegativeY,

    /// <summary>
    /// The direction pointing to negative Z.
    /// </summary>
    NegativeZ,

    /// <summary>
    /// The direction pointing to positive X.
    /// </summary>
    PositiveX,

    /// <summary>
    /// The direction pointing to positive Y.
    /// </summary>
    PositiveY,

    /// <summary>
    /// The direction pointing to positive Z.
    /// </summary>
    PositiveZ
}
